/*
主要介绍Math对象的一些方法
*/
// 1、Math.abs() 取绝对值
console.log(Math.abs(-10))
console.log(Math.abs(100))
// 2、Math.ceil() 向上取整 Math.floor()向下取整
console.log(Math.ceil(-15.01))
console.log(Math.floor(-15.01))
// 3、Math.random() 取[0,1)的随机数
console.log(Math.random()*10)
// 4、Math.round() 四舍五入
console.log(Math.round(-15.01))
// 5、Math.pow(x,y) x的y次幂
console.log(Math.pow(2,5))
// 6、Math.sqrt(x) 对x进行开方
console.log(Math.sqrt(16))
// 7、Math.max(x,y,z) Math.min(x,y,z) 在x,y,z中取最大值、最小值
console.log(Math.max(1,2,4,6))
console.log(Math.min(1, 2, 4, 6))
// 8、Math.PI  取圆周率的值
console.log(Math.PI)
